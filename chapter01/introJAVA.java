	//  -      
	//  - JAVAC 컴파일러    -(ctrl s)
	//  -
	//  - (파일명).class 파일 생성 = Byte-code)
	//  -
	//  - Java (파일명) 실행
	//  - 
	//  - JVM(인터프리터) -> 실행결과 출력
	
