	//    -> JVM(Java Virtual Machine)을 통해서 가능!
	//     * JVM: JAVA 프로그램을 실행할 때 반드시 필요한 머신
	//     -> 개발코드는 동일: Linux JVM, Unix JVM       --jre에 들어있음
	// - 메모리 관리 안해도 됨!
	//   - C언어의 경우 메모리 관리를 개발자가 직접해줘야함
	//   - JAVA는 메모리관리 없이 Garbage Collector가 알아서 처리해 줌
	
	// 3. JAVA 코드 실행 과정 --- *시험*
